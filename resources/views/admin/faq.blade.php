                        ордер не закроется на позиции и придется ждать "ретеста" уровня.</p>
                </div>
                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">3. Моя задача по BnB достигла тейк профита и отменилась, а
                        должна была продаться. Почему?</H3>
                    <p>
                        Дело в том, что наши задачи продают ровно столько - сколько купили, но во время действия этой
                        задачи, ваши BnB уменьшились (скорее всего из-за оплаты комиссии) и задача закрылась с ошибкой.
                        Рекомендуем держать запасные BnB на аккаунте, либо выставлять на тейк профит меньшее кол-во
                        монет, чем вы купили.</p>
                </div>
                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">4. Как работает стоп лосс трейлинг?</H3>
                    <p>Стоп лосс трейлинг движется за ценой на указанном вами изначально расстоянии. Как только цена
                        поднимается на Х единиц вверх - стоп лосс двигается на такое же расстояние.

                        Так например, Вы купили актив по 100 ед., выставили стоп лосс на 90. Цена поднялась до 108,
                        Trailing стоп лосс — 98. Если цена упала до 98- происходит продажа.</p>
                </div>

                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">5. Как работает тейк профит трейлинг?</H3>
                    <p>Тейк профит трейлинг включается при достижении последнего тейк профита на заданном расстоянии Х и
                        движется за растущей ценой, до тех пор, пока цена не упадет от своего максимума на Х процентов.

                        Так например, Ваш тейк профит стоял на отметке 100 ед. с трейлингом 1%. Цена поднимается до 105,
                        после чего падает до 104, где происходит фиксация.</p>

                </div>

                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">6. Как работает трейлинг лесенкой?</H3>
                    <p>Как только цена достигает первого тейк профита, Ваш стоп лосс автоматически передвигается на
                        уровень покупки (точка безубыточности). Если цена достигла второго тейк профита - стоп лосс
                        передвигается на уровень первого тейк профита и так далее пока задача не закроется.</p>

                </div>

                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">7. Нажал на “паник селл” и у меня уменьшился профит в
                        задаче.</H3>
                    <p>Паник селл продает маркетным ордером. Если у вас большой объем, то он может “продавить” цену
                        вниз.</p>

