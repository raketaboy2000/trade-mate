                    <p>Скорее всего у вас был небольшой размер ордера. Например, на бинансе минимальный размер ордера
                        0.001 BTC. Допустим вы купили XRP на 0.0019, выставили первый тейк на 0.001 и второй на 0.0009.
                        Таким образом наша система продаст весь ваш актив на первом тейке, т.к второй ордер не
                        отправится на биржу из-за непрохождения условия на минимальный размер ордера на бирже.</p>

                </div>


                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">14. Создал задачу на смарт трейд, но на бирже вижу только ордер
                        на покупку, а тейков нет.</H3>
                    <p>Мы не выставляем сразу ордера на биржу, чтобы не замораживать деньги. Как только цена достигнет
                        уровня тейк профит - наш сервис моментально отправит ордер.</p>

