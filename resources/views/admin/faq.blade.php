                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">8. Каждый раз при закрытии задачи на Binance у меня на балансе
                        остаются небольшие части монеток, я хочу чтобы продавалось все, как мне быть?</H3>
                    <p>Для того, чтобы у вас не оставалось крошечных частей монет, вам следует всего лишь включить
                        комиссию за BNB и иметь небольшой баланс этой монеты на счету, таким образом комиссия будет
                        списываться не с покупаемой монеты, и мы продадим полностью столько - сколько купили.</p>

                </div>

                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">9. Как подключить биржу?</H3>
                    <p>Перейдите во вкладку профиль. В разделе биржевые аккаунты выберите вашу биржу, скопируйте API
                        ключи с биржи (Key и Secret) и вставьте в соответствующие поля. Нажмите на кнопку «Добавить
                        аккаунт». После чего на Дашборде у вас появится окно вашего депозита на бирже (Обновление может
                        занять несколько минут)</p>

                </div>


                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">10. Что такое стоп лимит цена при покупке?</H3>
                    <p>Стоп лимит buy - это отложенный ордер на покупку.

                        Допустим, цена ходит в коридоре 100-110 и вы знаете что, пробив коридор, цена улетит выше. В
                        таком случае вы ставите стоп лимит на уровне 110.5, и цену 111. Это значит что при достижении
                        цены 110.5, наш сервис выставит ордер на покупку за 111 ед.</p>

                </div>

                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">11. Зачем нужна вкладка «Быстрая сделка» в Смарт трейде?</H3>
                    <p>Это функция быстрой покупки или продажи актива по маркетной цене без создания задачи. Бывают
                        экстренные ситуации, когда нет времени заполнять поля тейков и стопов, вы можете купить в этой
                        вкладке, и потом уже создать смарт трейд без покупки, указав свою цену.</p>

                </div>


                <div class="accordionItemContent_flex">
                    <H3 class="accordionItemContent_h3">12. Зачем нужна вкладка «Обменять все» в Смарт трейде?</H3>
                    <p>Во вкладке «Обменять все» вы сможете продать все свои активы в один клик.</p>

                </div>

                <div class="accordionItemContent_flex">
